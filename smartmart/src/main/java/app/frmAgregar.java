    private void updateCmb() {
        String sql = "SELECT descripcion FROM inventario_smartmart";

        try {
            pr = BD.conectar.prepareStatement(sql);
            rs = pr.executeQuery();

            // Agregar los elementos al JComboBox
            while (rs.next()) {
                cmbArticulo.addItem(rs.getString("descripcion"));
            }
        } catch (Exception e) {
            System.out.println("Error al agergar objeto: " + e.getMessage());
        }
    }

    public void obtenerCodigoArticulo(String idSeleccionado, JTextField txtCodigo) {

        // Consulta para obtener el nombre del artículo basado en el ID seleccionado
        String sql2 = "SELECT codigo FROM inventario_smartmart WHERE descripcion = ?";

        try {
            pr = BD.conectar.prepareStatement(sql2);
            pr.setString(1, idSeleccionado);
            rs = pr.executeQuery();

            // Mostrar el nombre del artículo en el JTextField
            if (rs.next()) {
                String nombreArticulo = rs.getString("codigo");
                txtCodigo.setText(nombreArticulo);
            }
        } catch (Exception e) {
            System.out.println("Error al obtener nombre del artículo: " + e.getMessage());
        }
    }
    
    
