    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        // TODO add your handling code here:
        String valor = txtCantidadAgregar.getText();
        // Obtener la cantidad a agregar del JTextField
        int cantidadAAgregar = Integer.parseInt(txtCantidadAgregar.getText());
        // Obtener el valor seleccionado en el JComboBox
        String articuloSeleccionado = (String) cmbArticulo.getSelectedItem();
        if (valor.equals("0")) {
            JOptionPane.showMessageDialog(null, "No se pueden agregar 0 elementos");
        }else{
            int respuesta = JOptionPane.showConfirmDialog(null, "¿Esta seguro de querer agregar " + valor +"?");
            
            if (respuesta == JOptionPane.YES_OPTION){
                String sql = "UPDATE inventario_smartmart SET inventario = inventario + ? WHERE descripcion = ?";
                try {
                    pr = BD.conectar.prepareStatement(sql);
                    pr.setInt(1, cantidadAAgregar);
                    pr.setString(2, articuloSeleccionado);
                    
                    int filasAfectadas = pr.executeUpdate();
                    if (filasAfectadas > 0) {
                        JOptionPane.showMessageDialog(null, "Cantidad actualizada en inventario correctamente");
                        txtCantidadAgregar.setText("0");
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al actualizar la cantidad en inventario");
                    }
                } catch (Exception e) {
                    System.out.println("Error al actualizar la cantidad en inventario: " + e.getMessage());
                }
            }else{
                
            }
        }
        
    }//GEN-LAST:event_btnAgregarMouseClicked

